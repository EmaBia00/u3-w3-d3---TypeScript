// Spiegazione Generica di TypeScript
//    E' Un linguaggio di programmazione open source e si tratta di un super-set di JavaScript sviluppato da Microsoft.
//    Permette di scrivere applicazioni più robuste e mantenibili grazie alla tipizzazione statica.
//    Gestisce i tipi delle variabili in fase di sviluppo, evitando errori durante l'esecuzione.

// Differenze con JavaScript:
// o	Tipizzazione statica: In TypeScript si definiscono i tipi delle variabili (es. string, number).
// o	Sintassi aggiuntiva: Supporta funzionalità come interfacce, generics.
// o	Compilazione: TypeScript deve essere compilato in JavaScript per essere eseguito.

// Il Compilatore TS
//    Trasforma il codice TypeScript in JavaScript eseguibile. Aiuta anche a trovare errori durante la compilazione.
// Come si usa:
// 1.	Installazione: npm install -g typescript.
// 2.	Compilazione: tsc nomefile.ts per generare un file .js.
// 3.	Configurazione: Uso del file tsconfig.json per personalizzare la compilazione.

